#include "libft.h"

int		ft_isnbr(char *str)
{
	int		i;

	i = 0;
	if (!str)
		return (0);
	while (str[i] != '\0')
	{
		if ((str[i] < 48 || str[i] > 57) && str[i] != '-' && str[i] != '+')
			return (0);
		i++;
	}
	return (1);
}
